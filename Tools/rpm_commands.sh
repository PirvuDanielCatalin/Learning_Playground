##################
## RPM commands ##
##################

# ==============================================================
# Install RPM
# ==============================================================
rpm -ivh ${rpm_name}
# or
yum install ${rpm_name}
# ==============================================================


# ==============================================================
# Upgrade RPM
# ==============================================================
rpm -Uvh ${rpm_name}
# or
yum update ${rpm_name}
# ==============================================================


# ==============================================================
# Uninstall RPM
# ==============================================================
rpm -e ${rpm_name}
# or
yum remove ${rpm_name}
# ==============================================================


# ==============================================================
# Unarchive RPM
# ==============================================================
tar xvf ${rpm_name}
# ==============================================================