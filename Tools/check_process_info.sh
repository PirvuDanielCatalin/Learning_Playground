# ==============================================================
# Check environment
# ==============================================================
component_name='...' && \
    pid=$(ps -ef | grep -v grep | grep "${component_name}" | awk '{print $2}') && \
    strings /proc/${pid}/environ
# ==============================================================


# ==============================================================
# Check live resource usage using htop
# ==============================================================
component_name='...' && \
    pid=$(ps -ef | grep -v grep | grep "${component_name}" | awk '{print $2}') && \
    htop -p ${pid}
# ==============================================================